Automatic parking systems (APS) are structures where cars are stacked vertically to limit space. The designs of these unique systems help transport vehicles from the entrance to its parking space without the driver present. In addition to the automatic parking system (APS), these vertical parking structures are also known by several other names:
</center></br></br></br>
<ul>
<li>Robotic parking garage</li>
<li>Car parking system</li>
<li>Automated parking facility (APF)</li>
<li>Automated vehicle storage and retrieval system (AVSRS)</li>
<li>Mechanical parking</li>
</ul>
</br></br>
<center>
<h3>
How it works: Drivers take their cars to the entrance of the automatic parking system where all occupants exit the vehicle. From here, the vehicle is moved by mechanical maneuvers to an available space where it is automatically parked or parked by an attendant.
